package day07_UnaryOperators;

public class OperatorProcedence {

    public static void main(String[] args) {

        int x = 2 + 3 * 2 / 1;

        System.out.println(x);

        int y = (2 + 3) * 2;

        System.out.println(y);

        int z = 2 + 3 * 2;

        System.out.println(z);


    }
}
