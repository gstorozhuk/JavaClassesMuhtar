package day03_Variables;

public class EscapeSequences {

    public static void main(String[] args) {

        System.out.println("Hello Everyone");
        System.out.println("My name is Galyna, I'm from Ukraine");
        System.out.println("-----------------");


        System.out.println("Hello Everyone\nMy name is Galyna, I'm from Ukraine");


// \t; new paragraph

        System.out.println("\tToday I learned Java");

// \\  system will recognize it as Escape Sequences so you need to use double

        System.out.println("\\");
        System.out.println("my name is \'Galyna\'");
        System.out.println("my name is 'Galyna'");
        System.out.println("-------------");

        System.out.println("my name is \"Galyna\"");









    }
}
