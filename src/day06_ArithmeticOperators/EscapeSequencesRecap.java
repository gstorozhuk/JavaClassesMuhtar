package day06_ArithmeticOperators;

public class EscapeSequencesRecap {

    public static void main(String[] args) {


/*
        Escape sequences: for text
        \n: append new line
        \t: tab space, paragraph space
        \\: \
        \": double quote
 */

        System.out.println("\tHello Everyone,\n\tHello Batch 21\n\tToday I learned 'Java'");
        System.out.println("===========================================");
        System.out.println("My favourite quote is: \n\t\"Winter is coming\"\n\t\t\t\t- John Snow");






    }
}
